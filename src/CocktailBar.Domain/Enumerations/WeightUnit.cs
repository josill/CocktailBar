namespace CocktailBar.Domain.Enumerations;

/// <summary>
/// Represents the units of measurement for weight.
/// </summary>
public enum WeightUnit
{
    /// <summary>
    /// Weight in grams.
    /// </summary>
    G,

    /// <summary>
    /// Weight in kilograms.
    /// </summary>
    Kg,

    /// <summary>
    /// Weight in millilitres.
    /// </summary>
    Ml,

    /// <summary>
    /// Weight in litres.
    /// </summary>
    L,

    /// <summary>
    /// Weight in pieces.
    /// </summary>
    Piece,

    /// <summary>
    /// Weight in slices.
    /// </summary>
    Slice,

    /// <summary>
    /// Weight in dashes.
    /// </summary>
    Dash,

    /// <summary>
    /// Weight in sprigs.
    /// </summary>
    Sprig,

    /// <summary>
    /// Weight in leaves.
    /// </summary>
    Leaves,
}
